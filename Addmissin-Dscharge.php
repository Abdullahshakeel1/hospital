      <h1 class="h1-bg-admision p-5 fw-bolder display-5">Admission and Discharge Procedures</h1>
      <div class="container-fluid px-5 pb-5">
      
        <h1 class="h1-all">Admission Process</h1>
        <h3 class="h3-all">Patients are usually admitted to the hospital either</h3>
        <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
            Through Emergency (Emergency Admission) or</span><br>
            <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                    Through Outpatients Department (Elective Admission)</span>
                    
                    <h3 class="h3-all">Elective admissions are either for</h3>
                    <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                        Daycase Procedures where the patients are discharged on the same day</span><br>
                        <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                            For Major Cases wherein the patients are admitted to the room.</span>


                            <h1 class="h1-all">Admission Process</h1>
                            <h3 class="h3-all">For Elective Admission, patients report to the Hospital Reception with the following documents</h3>
                            <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                               
                                  Admission slip stating the reason for admission and the procedure.</span><br>
                                  <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                                    Patient CNIC</span> <br>
                                    <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                                        Admission slip stating the reason for admission and the procedure.</span><br>
                                        <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                                            Insurance Card (If applicable)</span><br>
                                            <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                                                
The patient is also supposed to bring his latest prescription, investigation and relevant scans</span>





                                    <h3 class="h3-all">The staff at RECEPTION would</h3>
                                    <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                                        Make the patient’s File</span><br>
                                        <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                                            Send the patient to the Emergency Room in preparation for admission</span><br>
                                        <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                                            Send the  patient’s attendant to Accounts Department for payment</span>


                                            <h3 class="h3-all">In the EMERGENCY ROOM</h3>
                                            <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                                                The patient is prepared for the procedure/admission</span><br>
                                                <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                                                    He/she is changed into the Hospital gown if he is a day case otherwise transferred to his room.</span><br>
                                                    <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                                                       
                                                   The doctor Clarks the patient and obtains the consent</span><br>
                                                <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                                                  
The patient who is is to be admitted as a day case is transferred to the Operating Theatre.</span>




                                                    <h1 class="h1-all">Discharge Process</h1>
                                                    <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                                                        The patient is discharged as per instructions of the admitting Consultant.</span><br>
                                                        <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                                                            The discharge summary is entered in the ERP and the discharge slip is prepared by the Medical Officer</span><br>
                                                            <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                                                                After the discharge slip is made the patient’s bill is finalized by the Pharmacy, Laboratory, Radiology, and Physiotherapy.</span><br>
                                                                <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                                                                    The file is forwarded to the Accounts Department.</span><br>
                                                                    <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                                                                        The Accounts Department will call the patient’s attendants to settle their final bill.</span><br>
                                                        <i class="fa-solid fa-check-double text-danger fs-4"></i>   <span class="text-dark  fs-5 ">
                                                            The discharge slip is signed by the Medical Officer on the Floor and the patient is allowed to leave the Hospital.</span>
